package pbo_inheritance;
public class Matpel {
    public String Senin;
    public String Selasa;
    public String Rabu;
    public String Kamis;
    public String Jumat;
    
    public Matpel(String senin, String selasa, String rabu,
            String kamis, String jumat)
    {
        Senin = senin;
        Selasa = selasa;
        Rabu = rabu;
        Kamis = kamis;
        Jumat = jumat;
    }
    
    
}
